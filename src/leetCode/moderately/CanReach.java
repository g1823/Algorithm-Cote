package leetCode.moderately;

/**
 * @description: 1871. 跳跃游戏 VII
 */
public class CanReach {

    /**
     * 动态规划+前缀和
     * 解题思路：
     * 这道题第一眼非常像动态规划，因为当前位置是否可达，仅依赖前面某些位置是否可达，
     * 并且不关心前面位置是如何到达的，因此天然满足：
     * 1、最优子结构（这里本质是“可达性结构”）
     * 2、无后效性
     * 因此可以定义：
     * - dp[i]-表示下标i是否可达。
     * 如果当前位置i可以到达，则一定存在某个位置j满足：
     * 1、dp[j] == true
     * 2、i-j-位于[minJump,maxJump]之间
     * 因此可以得到状态转移：
     * dp[i]-=-是否存在某个j满足：
     * j∈[i-maxJump,i-minJump]
     * 且dp[j]-== true
     * 于是问题变成：
     * 对于每个位置i，需要判断：区间[i-maxJump,i-minJump]中，是否存在一个可达位置。
     * --------------------------------------------------
     * 最朴素的做法：
     * 对于每个i：
     * 1、遍历整个区间
     * 2、检查是否存在dp[j]-== true
     * 时间复杂度：
     * - O(n * (maxJump-minJump))
     * 最坏情况下：
     * - O(n²)
     * 会超时。
     * --------------------------------------------------
     * 这道题真正的核心考察点：
     * 不是动态规划本身，而是：如何优化“区间判断”。
     * 即：
     * 当状态转移依赖一个区间时，如何避免每次重复遍历整个区间。
     * --------------------------------------------------
     * 本题进一步分析后会发现，实际上并不需要知道：
     * 1、区间里有哪些可达点
     * 2、可达点具体在哪里
     * 只需要知道：
     * 区间内是否存在至少一个可达点。
     * 这属于典型的：“区间信息压缩”，即：
     * 从：
     * “获取整个区间所有元素”
     * 转换成：
     * “只获取区间聚合信息”
     * --------------------------------------------------
     * 本题本质上是：
     * 区间OR查询。
     * 因为：
     * dp[i]=dp[l]||dp[l+1]||...||dp[r]
     * 又由于：
     * boolean-OR等价于：“是否存在true”
     * 因此：
     * 又等价于：“true的数量是否大于0”
     * 于是问题进一步转化成： 区间计数问题。
     * --------------------------------------------------
     * 区间问题常见优化方式：
     * 1、区间和/区间个数
     * -前缀和
     * -树状数组
     * -线段树
     * 2、区间最大值/最小值
     * -单调队列
     * -线段树
     * -ST表
     * 3、区间是否存在某种元素
     * -计数
     * -前缀和
     * -位运算
     * -哈希
     * 4、动态滑动区间
     * -滑动窗口
     * -双指针
     * --------------------------------------------------
     * 本题有两种经典优化方案：
     * 一、前缀和优化DP
     * 定义：
     * pre[i]-表示[0,i]范围内可达元素个数。
     * 即：pre[i]-=-pre[i-1]-+-dp[i]
     * 那么：区间[left,right]中的可达元素数量：count = pre[right]-pre[left-1]
     * 若： count->-0，则说明：当前区间内存在可达点，
     * 即当前位置可达。
     * 这样即可将：每次O(区间长度)的区间遍历，优化成：O(1)区间查询。
     * 总时间复杂度：O(n)
     * 空间复杂度：O(n)
     * --------------------------------------------------
     * 二、滑动窗口优化
     * 本题还有一个非常经典的优化方式： 滑动窗口。
     * 因为对于位置i：
     * 可达区间为：[i-maxJump,i-minJump]
     * 当i增加1时：
     * 区间会整体右移一位：[i+1-maxJump,i+1-minJump]
     * 即：
     * 1、左边有元素离开窗口
     * 2、右边有元素进入窗口
     * 因此可以维护：当前窗口内可达元素数量。
     * 每次：
     * 1、新元素进入窗口时加入统计
     * 2、旧元素离开窗口时移除统计
     * 若：窗口内可达元素数量大于0，则当前位置可达。
     * 这种方式本质上是：
     * 动态维护区间统计信息，而不是重复扫描整个区间。
     * 时间复杂度：O(n)
     * 空间复杂度： O(n)
     * 若直接复用dp数组，
     * 甚至可以进一步优化空间。
     * --------------------------------------------------
     * 本题最核心的思维提升：
     * 当看到：dp[i]依赖某个区间时，
     * 不要立刻想到：for循环枚举整个区间。
     * 而应该思考：“当前真正需要区间里的什么信息”
     * 是：
     * 1、和
     * 2、个数
     * 3、是否存在
     * 4、最大值
     * 5、最小值
     * 然后再考虑，是否能通过：
     * 1、前缀和
     * 2、滑动窗口
     * 3、单调队列
     * 4、树状数组
     * 5、线段树
     * 等方式，
     * 避免重复区间遍历。
     */
    public boolean canReach(String s, int minJump, int maxJump) {
        int n = s.length();
        // dp[i] 表示下标i是否可达, pre[i] 表示从0到下标i之间可达元素个数（即前缀和）
        int[] dp = new int[n];
        int[] pre = new int[n];
        // 初始化，下标0可达，可达元素个数为1
        dp[0] = 1;
        pre[0] = 1;
        char[] chars = s.toCharArray();
        for (int i = 1; i < n; i++) {
            // 当前元素为'0'时才可达
            if (chars[i] == '0') {
                // 计算可到达当前元素的区间范围
                int left = i - maxJump;
                int right = i - minJump;
                // 通过区间左右前缀和之差，计算当前区间内可达元素个数
                if (right >= 0) {
                    int leftCount = left > 0 ? pre[left - 1] : 0;
                    // 当前区间内可达元素个数大于0，则说明当前元素可达
                    if (pre[right] - leftCount > 0) {
                        dp[i] = 1;
                    }
                }
            }
            // 计算前缀和
            pre[i] = pre[i - 1] + dp[i];
        }
        return dp[n - 1] > 0;
    }

    /**
     * 滑动窗口优化版本
     * 维护区间 [i-maxJump, i-minJump] 内可达点数量
     */
    public boolean canReach2(String s, int minJump, int maxJump) {
        int n = s.length();
        char[] chars = s.toCharArray();
        int[] dp = new int[n];
        dp[0] = 1;
        // 当前窗口内可达点数量
        int reachable = 0;
        for (int i = 1; i < n; i++) {
            // ---- 1、窗口右端进入新元素：i - minJump ----
            int addIndex = i - minJump;
            if (addIndex >= 0 && chars[addIndex] == '0') {
                reachable += dp[addIndex];
            }
            // ---- 2、窗口左端移出元素：i - maxJump - 1 ----
            int removeIndex = i - maxJump - 1;
            if (removeIndex >= 0 && chars[removeIndex] == '0') {
                reachable -= dp[removeIndex];
            }
            // ---- 3、判断当前 i 是否可达 ----
            if (chars[i] == '0' && reachable > 0) {
                dp[i] = 1;
            }
        }
        return dp[n - 1] == 1;
    }
}
