package leetCode.moderately;

import java.util.HashSet;
import java.util.PriorityQueue;
import java.util.Set;

/**
 * @author: gj
 * @description: 2336. 无限集中的最小数字
 */
class SmallestInfiniteSet {
    // 无限集合中还未pop过的最小数
    int cur = 1;
    // 最小堆
    PriorityQueue<Integer> pq = new PriorityQueue<>();
    // 优先队列不会去重，因此需要去重
    Set<Integer> inHeap = new HashSet<>();

    /**
     * - ======================= 思考过程与抽象建模 =======================
     * - 一、最初直觉
     * - ------------------------------------------------
     * - 题目给的是一个“无限正整数集合”，支持：
     * - 1）popSmallest：取出并删除最小值
     * - 2）addBack：把删除过的数加回去
     * - 第一反应很容易想到“最小值 + 动态变化”，因此会自然联想到堆。
     * - 但如果直接尝试：
     * -   - 用堆维护当前集合
     * - 就会立刻遇到问题：
     * -   - 无限集合如何初始化？
     * -   - 哪些数本来就在集合中，哪些是 addBack 回来的？
     * -   - 如何防止重复？
     * - 这说明：问题不在“堆怎么写”，而在于没有抽象出稳定的不变量。
     * - 二、关键卡点：没有抽象出「连续区间」
     * - ------------------------------------------------
     * - 真正困难的地方在于：
     * -   - 集合表面是“无限的”
     * -   - 但 pop 操作具有强烈的“从小到大”的顺序性
     * - 如果不对“从未 pop 过的最小数”做统一抽象，
     * - 那么所有逻辑都会变成：
     * -   - 无限状态 + 零散回溯
     * -   - 实现和推理都会非常复杂
     * - 三、核心抽象（本题的灵魂）
     * - ------------------------------------------------
     * - 引入一个变量 cur：
     * -   cur 表示：当前「还从未被 pop 过的最小正整数」
     * - 那么可以得到一个非常关键的不变量：
     * -   当前集合 等价于：
     * -     { 所有被 addBack 回来的、且小于 cur 的数 }
     * -       ∪
     * -     { cur, cur+1, cur+2, ... }
     * - 也就是说：
     * -   - >= cur 的所有数，一定都在集合中（不需要显式维护）
     * -   - < cur 的数，只有被 addBack 过的，才在集合中
     * - 这样，“无限集合”被压缩成：
     * -   - 一个整数 cur
     * -   - 一个有限集合（addBack 回来的数）
     * - 四、数据结构选择的自然结果
     * - ------------------------------------------------
     * - 在上述抽象下，需求变成：
     * -   - 对 addBack 回来的数：
     * -       * 能快速取最小值
     * -       * 需要去重
     * - 因此采用：
     * -   - PriorityQueue（最小堆）：维护 addBack 的数
     * -   - HashSet：防止重复加入堆
     * - 堆中始终只存：
     * -   - 严格小于 cur 的数字
     * - 五、popSmallest 的决策逻辑（基于不变量）
     * - ------------------------------------------------
     * - 每次 pop 时，只需在两个候选中取最小：
     * -   1）堆顶元素（如果存在）
     * -   2）cur
     * - - 如果堆非空，且堆顶 < cur：
     * -     说明这是一个被 addBack 回来的、更小的数
     * -     → pop 堆顶
     * - - 否则：
     * -     说明 cur 是当前集合中最小值
     * -     → 返回 cur，并令 cur++
     * - 这一过程不会破坏不变量。
     * - 六、addBack 的边界判断（最容易出错的地方）
     * - ------------------------------------------------
     * - addBack(num) 并不是“无条件加入”：
     * - - 如果 num >= cur：
     * -     说明该数本来就在集合中
     * -     → 本次 addBack 是无效操作，必须忽略
     * - - 只有当：
     * -     num < cur 且 当前不在堆中
     * -   才需要将其加入堆
     * - 否则会导致：
     * -   - 重复元素
     * -   - 破坏最小性
     * - 七、总结
     * - ------------------------------------------------
     * - 这道题的关键不在于堆的使用，
     * - 而在于：
     * -   用一个变量 cur 表示“连续的无限区间”，
     * -   用一个最小堆维护“被打断的离散部分”。
     * - 一旦该不变量建立，代码实现是自然且直接的。
     */
    public SmallestInfiniteSet() {

    }

    public int popSmallest() {
        if (pq.isEmpty()) {
            return cur++;
        } else {
            Integer poll = pq.poll();
            inHeap.remove(poll);
            return poll;
        }
    }

    public void addBack(int num) {
        if (num < cur) {
            if (!inHeap.contains(num)) {
                pq.add(num);
                inHeap.add(num);
            }
        }
    }
}