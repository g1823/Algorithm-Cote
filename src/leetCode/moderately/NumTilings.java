package leetCode.moderately;

/**
 * @author: gj
 * @description: 790. 多米诺和托米诺平铺
 */
public class NumTilings {
    private static final int MOD = 1_000_000_007;

    /**
     * - 动态规划
     * - 一、最初思路：枚举“组合类型”
     * - ------------------------------------------------------------
     * - 一开始尝试从“可用的基础组合”入手分析：
     * - 2×1、2×2、2×3、2×4 等有限宽度的组合类型，
     * - 认为更大的铺法都是这些基础组合的拼接，
     * - 并希望通过“当前位置 i - 组合宽度”的方式进行转移。
     * - 但很快发现问题：
     * - - 托米诺（L 形）会造成局部不规则缺口
     * - - 无法只用“完整矩形组合”描述中间状态
     * - - 组合类型并非独立原子，拼接顺序会影响可行性
     * - 结论：单纯枚举组合类型不可行，必须显式刻画“边界状态”。
     * - 二、修正方向：从左到右，刻画列边界状态
     * - ------------------------------------------------------------
     * - 转而采用“按列推进”的动态规划模型：
     * - 每一步只考虑当前列及其对下一列的影响，
     * - 并通过状态记录“第 i+1 列已经被占用的情况”。
     * - 这一步的关键认知是：
     * - - dp 的状态不只是“铺到第 i 列”
     * - - 还必须包含“下一列是否被提前占用”
     * - 三、dp 状态定义（关键转折点）
     * - ------------------------------------------------------------
     * - 定义 dp[i][s]：
     * - 表示【前 i 列已经完全合法铺满】后，
     * - 【第 i+1 列】的占用状态为 s。
     * - s 有四种可能：
     * - 0：第 i+1 列，上空，下空
     * - 1：第 i+1 列，上占，下空
     * - 2：第 i+1 列，下占，上空
     * - 3：第 i+1 列，上占，下占
     * - 这里一度出现过理解偏差：
     * - - 错把“dp[i] 的状态”当成“第 i 列本身的状态”
     * - - 导致分析中出现：
     * -   “i-1 的状态影响 i，i 的放置又隐式影响 i+1”
     * -   甚至出现“放一个牌占到 i+2 列却没被记录”的矛盾
     * - 修正后的关键原则：
     * - - 所有放置操作，最多只允许影响到第 i+1 列
     * - - 不能出现对 i+2 列的隐式占用
     * - - 否则状态不封闭，DP 不成立
     * - 四、逐一校验转移合法性，得到正确转移方程
     * - ------------------------------------------------------------
     * - 在上述状态语义下，逐一检查每个状态的合法来源：
     * - dp[i][0]（第 i+1 列上下都空）：
     * - - 来自 dp[i-1][0]：第 i 列空，竖放 2×1
     * - - 来自 dp[i-1][3]：第 i 列已满，不放任何牌
     * - dp[i][1]（第 i+1 列上占下空）：
     * - - 来自 dp[i-1][0]：通过一个 L 型托米诺
     * - - 来自 dp[i-1][2]：补齐缺口，不产生越界
     * - dp[i][2]（第 i+1 列下占上空）：
     * - - 与 dp[i][1] 对称
     * - dp[i][3]（第 i+1 列上下都占）：
     * - - 来自 dp[i-1][0]：两个横向 1×2
     * - - 来自 dp[i-1][1]：一个 L 型补齐
     * - - 来自 dp[i-1][2]：镜像 L 型补齐
     * - 五、最终目标状态
     * - ------------------------------------------------------------
     * - 题目要求 2×n 完全铺满，
     * - 即前 n 列铺完后，第 n+1 列必须“上下都空”：
     * - 返回 dp[n][0]
     * - 该四状态模型是封闭的、无越界的，
     * - 并且可以进一步等价收敛为：
     * - dp[n] = 2 * dp[n-1] + dp[n-3]
     */
    public int numTilings(int n) {
        // dp[i][s]: 前 i 列铺完后，第 i+1 列的状态
        long[][] dp = new long[n + 1][4];

        // 初始状态
        dp[0][0] = 1;

        for (int i = 1; i <= n; i++) {
            dp[i][0] = (dp[i - 1][0] + dp[i - 1][3]) % MOD;
            dp[i][1] = (dp[i - 1][0] + dp[i - 1][2]) % MOD;
            dp[i][2] = (dp[i - 1][0] + dp[i - 1][1]) % MOD;
            dp[i][3] = (dp[i - 1][0] + dp[i - 1][1] + dp[i - 1][2]) % MOD;
        }

        // 要求前 n 列铺满 → 第 n+1 列必须上下都空
        return (int) dp[n][0];
    }
}
