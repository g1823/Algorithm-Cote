package leetCode.moderately;

/**
 * @author: gj
 * @description: 714. 买卖股票的最佳时机含手续费
 */
public class MaxProfitSXF {

    /**
     * - 【问题抽象】
     * - - 可以进行多次买卖，每次完整交易（一次买 + 一次卖）需要支付一次手续费 fee
     * - - 最终一定是“不持有股票”的状态收益最大
     * - 【最初思路 & 卡点】
     * - - 问题形式上与 122（无限次交易）非常相似
     * - - 但由于引入了手续费：
     * -   - 如果把连续上涨区间拆成多次交易，会多次支付手续费
     * -   - 如果合并为一次交易，只需要支付一次手续费
     * - - 因此不能再简单地“吃掉所有上涨区间”，
     * -   交易次数本身变成了需要权衡的成本
     * - 【关键转化】
     * - - 手续费的本质：一次完整交易才会产生一次费用
     * - - 由于最终答案一定取“不持股”状态，
     * -   可以将手续费统一放到“买入成本”中，而不是在卖出时扣
     * - 【状态设计（沿用 122 的 DP 模型）】
     * - dp[i][0]：第 i 天结束后，不持有股票的最大收益
     * - dp[i][1]：第 i 天结束后，持有一股股票的最大收益
     * - 【状态转移含义】
     * - - dp[i][0]：
     * -   1）昨天持有股票，今天卖出（卖出时不再扣手续费）
     * -   2）昨天就不持有股票，今天不操作
     * - - dp[i][1]：
     * -   1）昨天不持有股票，今天买入（买入成本 = 股票价格 + 手续费）
     * -   2）昨天就持有股票，今天不操作
     * - 【核心思想总结】
     * - - 与 122 的 DP 解法结构完全一致
     * - - 唯一变化：在“买入”时将手续费并入成本
     * - - 是否拆分交易、是否值得多付一次手续费，
     * -   由状态转移中的 max 自动完成权衡
     */
    public int maxProfit(int[] prices, int fee) {
        // dp[i][0\1]记录当前的最大收益
        int n = prices.length;
        int[][] dp = new int[n][2];
        dp[0][0] = 0;
        dp[0][1] = -prices[0] - fee;
        for (int i = 1; i < n; i++) {
            // 第i天不持有股票的最大收益 = max((第i-1天持有股票最大价值，今天买出)，（第i-1天不持有股票最大价值，今天不操作））
            dp[i][0] = Math.max(dp[i - 1][1] + prices[i], dp[i - 1][0]);
            // 第i天持有股票的最大收益 = max((第i-1天不持有股票最大价值，今天买入,并扣除手续费)，（第i-1天持有股票最大价值，今天不操作））
            dp[i][1] = Math.max(dp[i - 1][0] - prices[i] - fee, dp[i - 1][1]);
        }
        return dp[n - 1][0];
    }
}
