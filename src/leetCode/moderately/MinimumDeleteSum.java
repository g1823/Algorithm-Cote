package leetCode.moderately;

/**
 * @author gaojie
 * @date 2026/1/10 21:51
 * @description: 712. 两个字符串的最小ASCII删除和
 */
public class MinimumDeleteSum {

    /**
     * - 动态规划
     * - 一、与常见字符串问题的对比与区分
     * - 1）与编辑距离（Edit Distance）的区别：
     * -    - 编辑距离允许 插入 / 删除 / 替换，且通常代价相同；
     * -    - 本题只允许“删除”操作，且删除代价为字符的 ASCII 值；
     * -    - 因此问题并不是“将 s1 转换为 s2”，而是：
     * -      在两个字符串上同时删除字符，使它们变为某个相同的字符串。
     * - 2）与背包问题的区别：
     * -    - 背包问题是对单个集合做选择（选 / 不选）；
     * -    - 本题的删除决策依赖两个字符串的相对位置，
     * -      不是独立选择，因此不适合用背包建模。
     * - 3）与最长公共子序列（LCS）的联系：
     * -    - 删除字符后，最终保留下来的字符串一定是：
     * -      同时是 s1 和 s2 的子序列；
     * -    - 问题本质是：在所有公共子序列中做最优选择。
     * -
     * - 二、从暴力枚举到最优子结构
     * - 可以从暴力 DFS 思路出发：
     * -   - 从 s1[i], s2[j] 开始：
     * -     - 若字符相等，可以同时保留，继续向后匹配；
     * -     - 若字符不等，则必须删除其中一个字符：
     * -       - 删除 s1[i]，或
     * -       - 删除 s2[j]，
     * -       并累加对应 ASCII 代价。
     * - 该递归结构与 LCS 的递归形式完全一致，
     * - 区别在于：
     * -   - LCS 求最大长度；
     * -   - 本题求最小删除代价。
     * -
     * - 由于存在大量重叠子问题，适合使用动态规划进行优化。
     * -
     * - 三、两种等价视角：保留 vs 删除
     * - 1）保留视角：
     * -    - 最终保留下来的公共子序列越“值钱”越好；
     * -    - 每个字符的“价值”是其 ASCII 值；
     * -    - 即：求 ASCII 和最大的公共子序列（带权 LCS）。
     * - 2）删除视角（本实现采用）：
     * -    - 直接以“最小删除 ASCII 和”为目标进行 DP；
     * -    - 不需要额外计算总和或做结果转换；
     * -    - 状态和目标更加直观。
     * -
     * - 两种视角本质等价，本题直接采用删除视角建模。
     * -
     * - 四、状态定义
     * - dp[i][j] 表示：
     * -   s1 的前 i 个字符（s1[0..i-1]）
     * -   与 s2 的前 j 个字符（s2[0..j-1]）
     * -   通过删除字符操作变为相同字符串时，
     * -   所需的最小 ASCII 删除和。
     * -
     * - 五、状态转移
     * - 1）当 s1[i-1] == s2[j-1]：
     * -    - 两个字符可以同时保留；
     * -    - 不产生任何删除代价；
     * -    - dp[i][j] = dp[i-1][j-1]。
     * -    说明：
     * -    由于删除任意字符都会引入一个严格大于 0 的代价，
     * -    且该代价不可能在后续被抵消，
     * -    因此在最优解中一定会保留这对相等字符。
     * -
     * - 2）当 s1[i-1] != s2[j-1]：
     * -    - 最终相同字符串的最后一个字符不可能同时来自二者；
     * -    - 必须删除其中一个字符：
     * -    dp[i][j] = min(
     * -        dp[i-1][j] + ASCII(s1[i-1]),   // 删除 s1[i-1]
     * -        dp[i][j-1] + ASCII(s2[j-1])    // 删除 s2[j-1]
     * -    )
     * -
     * - 六、边界条件
     * - 1）dp[0][j]：
     * -    - s1 为空，只能删除 s2 的前 j 个字符；
     * - 2）dp[i][0]：
     * -    - s2 为空，只能删除 s1 的前 i 个字符。
     * -
     * - 七、结果
     * - dp[m][n] 即为：
     * -   s1 与 s2 通过删除字符变为相同字符串时，
     * -   所需的最小 ASCII 删除和。
     * - 该解法时间复杂度 O(m * n)，空间复杂度 O(m * n)，
     * - 可在此基础上进一步进行空间压缩优化。
     */
    public int minimumDeleteSum(String s1, String s2) {
        int m = s1.length(), n = s2.length();
        // dp[i][j] 表示 s1 前 i 个字符 与 s2 前 j 个字符
        // 通过删除操作变为相同字符串时的最小 ASCII 删除和
        int[][] dp = new int[m + 1][n + 1];
        char[] chars1 = s1.toCharArray();
        char[] chars2 = s2.toCharArray();

        // s1 为空，只能删 s2
        for (int j = 1; j <= n; j++) {
            dp[0][j] = dp[0][j - 1] + chars2[j - 1];
        }

        // s2 为空，只能删 s1
        for (int i = 1; i <= m; i++) {
            dp[i][0] = dp[i - 1][0] + chars1[i - 1];
        }

        for (int i = 1; i <= m; i++) {
            for (int j = 1; j <= n; j++) {
                if (chars1[i - 1] == chars2[j - 1]) {
                    dp[i][j] = dp[i - 1][j - 1];
                } else {
                    dp[i][j] = Math.min(
                            dp[i - 1][j] + chars1[i - 1],
                            dp[i][j - 1] + chars2[j - 1]
                    );
                }
            }
        }
        return dp[m][n];
    }
}
