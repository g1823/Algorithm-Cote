package leetCode.moderately;

import java.util.HashSet;

/**
 * @description: 3043. 最长公共前缀的长度
 */
public class LongestCommonPrefix {

    /**
     * 解题思路：枚举前缀 + 哈希表
     * 一、先思考两个数字如何求最长公共前缀
     * 最直接的思路是：
     * - 转成字符串后逐位比较
     * - 或使用数学方式逐位取高位比较
     * 但继续观察会发现：
     * - 对于十进制数字来说，一个数字不断 /10，本质上就是不断删除最后一位。
     * 例如：
     * - 12345 -> 1234 -> 123 -> 12 -> 1
     * 而这些结果，恰好就是该数字的所有前缀。
     * 因此：
     * - 两个数字的公共前缀，本质上就是它们是否存在相同的“缩短结果”。
     * 例如：
     * - 12345 的前缀集合：
     * - 12345、1234、123、12、1
     * - 12399 的前缀集合：
     * - 12399、1239、123、12、1
     * 它们第一次相同的最长结果是：
     * - 123
     * 因此最长公共前缀长度为：
     * - 3
     * 所以：
     * - 使用 num /= 10 的方式，可以非常自然地得到一个数字的所有前缀。
     * ------------------------------------------------------------------------------------
     * 二、继续思考集合之间如何做
     * 最容易想到的方法是：
     * - 双重循环枚举 arr1 和 arr2 的所有数对
     * - 每对数字再求一次最长公共前缀
     * 但这样会存在大量重复计算。
     * 例如：
     * - arr2 中多个数字可能都包含前缀 123
     * - arr1 中每个数字都会重复重新计算一次
     * 因此真正应该缓存的并不是“数字”，而是：
     * - 所有可能前缀
     * ------------------------------------------------------------------------------------
     * 三、核心优化
     * 1、先遍历 arr1
     * - 将 arr1 中每个数字的所有前缀全部加入 HashSet
     * 例如：
     * - 12345 -> 12345、1234、123、12、1
     * HashSet 的作用：
     * - O(1) 判断某个前缀是否存在
     * 2、再遍历 arr2
     * - 对于当前数字，不断执行 num /= 10
     * - 判断当前前缀是否存在于 HashSet 中
     * 由于是从最长前缀开始缩短：
     * - 一旦命中
     * - 当前结果一定是该数字的最长公共前缀
     * - 后续更短前缀无需继续判断
     * ------------------------------------------------------------------------------------
     * 四、额外优化
     * 1、前缀去重优化
     * - 在构建 arr1 前缀集合时
     * - 如果当前前缀已经存在
     * - 则可以直接结束
     * 例如：
     * - 已处理过 12345
     * - 再处理 12399 时
     * - 当发现 123 已存在
     * - 则 12、1 必然也已经存在
     * - 可以直接停止
     * 2、长度剪枝优化
     * - 当前数字在不断 /10 后
     * - 位数会不断减少
     * 如果：
     * - 当前剩余位数 <= 当前最大答案
     * 则：
     * - 即使后续命中
     * - 也不可能更新答案
     * - 可以提前结束当前循环
     * ------------------------------------------------------------------------------------
     * 五、为什么使用 HashSet 而不是 Trie（前缀树）
     * Trie 更适合：
     * - 大量字符串前缀匹配
     * - 前缀统计
     * - 自动补全
     * - 前缀搜索
     * 但本题只需要：
     * - 判断某个前缀是否存在
     * 因此：
     * - HashSet<Integer>
     * 已经足够高效。
     * 同时：
     * - Trie 会存在额外节点对象开销
     * - 实现也更复杂
     * ------------------------------------------------------------------------------------
     * 六、时间复杂度
     * 假设：
     * - n = arr1.length
     * - m = arr2.length
     * - 每个数字最多 d 位
     * 则：
     * - 构建前缀集合：O(n * d)
     * - 查询公共前缀：O(m * d)
     * 总复杂度：
     * - O((n + m) * d)
     * 由于 d 最大仅为数字位数，因此整体复杂度接近线性。
     */
    public int longestCommonPrefix(int[] arr1, int[] arr2) {
        // 1、收集arr1的所有前缀
        HashSet<Integer> arr1Prefix = new HashSet<>();
        for (int i = 0; i < arr1.length; i++) {
            int num = arr1[i];
            addPrefix(num, arr1Prefix);
        }

        // 2、遍历arr2，判断是否存在arr1的子前缀
        int max = 0;
        for (int i = 0; i < arr2.length; i++) {
            int num = arr2[i];
            // 如果当前数比 10^最大前缀数 小，那么后续则不可能超过最大前缀了，可以直接结束
            while (num != 0 && getLength(num) > max) {
                if (arr1Prefix.contains(num)) {
                    max = Math.max(max, getLength(num));
                    // 当前元素一旦发现前缀则可以跳过，因为后续前缀更小
                    break;
                }
                num = num / 10;
            }
        }
        return max;
    }

    private void addPrefix(int num, HashSet<Integer> arr1Prefix) {
        while (num != 0) {
            // 如果当前前缀已经加入，则可以直接结束，因为剩余子前缀已经加入Set了
            if (arr1Prefix.contains(num)) {
                break;
            }
            arr1Prefix.add(num);
            num = num / 10;
        }
    }

    private int getLength(int num) {
        int len = 0;
        while (num != 0) {
            len++;
            num /= 10;
        }
        return len;
    }
}
