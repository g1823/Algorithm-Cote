package leetCode.difficult;

import java.util.Arrays;

/**
 * @description: 60. 排列序列
 */
public class GetPermutation {
    /**
     * 阶乘分组 + 模拟构造
     * 一、题目理解
     * 给定 1~n 这 n 个数字，要求返回其所有排列按字典序排序后的第 k 个排列。
     * 例如：n = 3
     * 全排列按字典序如下：
     * 1. 123
     * 2. 132
     * 3. 213
     * 4. 231
     * 5. 312
     * 6. 321
     * 题目本质：
     * 不是生成所有排列后排序，而是：“直接定位第 k 个排列是什么”
     * ----------------
     * 二、最开始的思考过程
     * 最开始的思路是：
     * 从字典序规律观察可以发现,排列变化时，最先变化的是后面的数字。
     * 例如：
     * n = 4
     * 1234、1243...
     * 只有最后两位变化。
     * 然后：
     * 1324、1342...
     * 说明：
     * 后两位有： 2! = 2种变化。
     * 后三位有：3! = 6种变化。
     * 后四位有：4! = 24种变化。
     * 因此可以想到：
     * 如果：3! < k <= 4!
     * 说明：第 k 个排列一定发生在“后四位变化”中，此时前 n-4 位一定固定。
     * 例如：n = 6, k = 14
     * 因为：3! = 6、4! = 24
     * 满足：6 < 14 <= 24
     * 因此：前两位固定：
     * 12 _ _ _ _
     * 然后继续在后四位中定位。
     * ----------
     * 三、为什么最终会转化为“逐位确定”
     * 虽然最开始是：“一次确定多位”，但继续推导会发现：排列实际上是“递归分块”的结构。
     * 例如：
     * - 1xxxx
     * -    12xxx
     * -        123xx
     * -        124xx
     * -    13xxx
     * -
     * - 2xxxx
     * 即：
     * 大块里面还有小块，小块里面还有更小的块。
     * 因此：即使确定了： “前两位固定”，后面仍然需要继续细分：
     * - 12 3xxx
     * - 12 4xxx
     * - 12 5xxx
     * - ...
     * 所以最终一定会演变成：“每次确定一位”，这也是本题标准解法的本质。
     * ------------
     * 四、核心规律（阶乘分组）
     * 对于当前剩余数字：假设还剩 m 个数字没有使用，
     * 那么：固定当前位后，后面的排列数量一定是：(m-1)!
     * 例如：n = 4
     * 所有排列：
     * - 1xxx -> 6种
     * - 2xxx -> 6种
     * - 3xxx -> 6种
     * - 4xxx -> 6种
     * 因为：3! = 6，所以：每一个前缀都对应固定数量的排列。
     * 因此：
     * 可以通过：k / (m-1)!，直接确定：当前位应该选剩余数字中的第几个。
     * -----------
     * 五、为什么 k 要减一（最关键）
     * 题目中的：“第 k 个排列”是：1-based（从1开始）
     * 例如：
     * - 第1个
     * - 第2个
     * - 第3个
     * 但程序中的：数组下标、块编号、整数除法，全部天然是：
     * 0-based（从0开始）
     * 例如：
     * 每块大小 = 6
     * 块划分：
     * 块0 -> 0~5
     * 块1 -> 6~11
     * 块2 -> 12~17
     * 假设：k = 14，实际上：14个排列，在程序中应该对应：下标13
     * 因此必须： k--，转换成：0-based 下标。
     * 这样：
     * index = k / size 才能正确表示：“当前落在哪一个块”
     * 否则会出现：第6个元素被错误划分到第1块的问题。
     * --------
     * 六、算法流程
     * 1、预处理 factorial[i] = i!
     * 2、维护一个“当前还未使用的数字集合”
     * 3、每次确定当前位：
     * -      当前块大小：size = (剩余数字数量 - 1)!
     * 4、计算：
     * -     index = k / size
     * -     表示：当前位应该选择：“剩余数字中的第 index 个”
     * 5、更新：
     * -      k %= size
     * -      表示：在当前块中继续查找。
     * 6、重复直到所有位置填完。
     * ----
     * 七、时间复杂度
     * 外层循环 n 次，每次需要遍历寻找第 index 个未使用数字。
     * 时间复杂度：O(n^2)
     * 空间复杂度：O(n)
     */
    public String getPermutation(int n, int k) {
        // factorial[i] 表示 i!
        int[] factorial = new int[n];
        factorial[0] = 1;
        // 预处理阶乘
        for (int i = 1; i < n; i++) {
            factorial[i] = factorial[i - 1] * i;
        }

        // nums[i] == 0 表示数字 i 还未使用
        // nums[i] == 1 表示数字 i 已被使用
        int[] nums = new int[n + 1];
        StringBuilder sb = new StringBuilder();

        /*
         * 将“第k个排列”转换成： “下标为 k-1 的排列”
         * 因为：程序中的：块编号、数组下标、整数除法，都是0-based。
         */
        k--;
        // 逐位确定答案
        for (int i = 0; i < n; i++) {
            /*
             * 当前块大小
             * 例如：
             * 当前还剩4个数字：
             * _ _ _ _
             * 固定当前位后：后面还有：3! = 6种排列。
             */
            int size = factorial[n - i - 1];

            // 当前应该选择：“剩余数字中的第 index 个”，index 是0-based。
            int index = k / size;

            // 进入当前块后，继续在块内查找。
            k %= size;

            // 从小到大寻找：第 index 个“未使用数字”
            for (int j = 1; j <= n; j++) {
                // 当前数字未使用
                if (nums[j] == 0) {
                    // index == 0 表示：当前数字就是：“剩余数字中的第 index 个”
                    if (index == 0) {
                        nums[j] = 1;
                        sb.append(j);
                        break;
                    }
                    // 继续寻找下一个未使用数字
                    index--;
                }
            }
        }

        return sb.toString();
    }
}
