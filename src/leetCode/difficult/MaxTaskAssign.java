package leetCode.difficult;

import java.util.ArrayDeque;
import java.util.Arrays;
import java.util.Deque;
import java.util.TreeMap;

/**
 * @author: gj
 * @description: 2071. 你可以安排的最多任务数目
 */
public class MaxTaskAssign {
    public static void main(String[] args) {
        int[] tasks = new int[]{5, 4};
        int[] workers = new int[]{0, 0, 0};
        System.out.println(new MaxTaskAssign().maxTaskAssign(tasks, workers, 1, 5));
    }

    /**
     * - TreeMap + 二分 + 固定k（在线决策转为离线决策，最优解转为能否解）
     * -
     * - 一、最初的直觉思路（以“工人”为中心的贪心）：
     * -
     * - 最开始的直觉是典型的贪心想法：
     * -  - 力量大的工人去搬重的货物
     * -  - 力量小的工人去搬轻的货物
     * - 看起来非常合理，也符合人类直觉。
     * -
     * - 于是自然会从“工人视角”出发思考：
     * -  - 当前这个工人，能不能搬这个货物？
     * -  - 如果搬不动，要不要现在喝药水？
     * -  - 药水现在用了，会不会导致后面更需要药水的工人没得用？
     * -
     * - 但很快会发现一个严重问题：
     * -  - 当前是否使用药水，会直接影响后续所有工人的选择空间
     * -  - 有的工人不喝药能搬轻的，喝药能搬重的
     * -  - 药水如果给错人，可能直接导致后续无法完成更多任务
     * -
     * - 于是会陷入：
     * -  - “现在喝 or 不喝？”
     * -  - “这个货物要不要先跳过？”
     * -  - “留给后面会不会更好？”
     * -
     * - 这种分析会出现“牵一发而动全身”的情况，
     * - 情况分支呈指数级增长，几乎无法穷举，
     * - 本质原因是：在一开始就试图做“在线决策（online decision）”。
     * -
     * - 二、关键转折：设立目标 —— 假设要搬运 k 个任务
     * -
     * - 真正的突破点在于：不再纠结“当前要不要做这个任务”，
     * - 而是反过来设立一个目标：
     * -
     * -  - 假设最终目标是：完成 k 个任务
     * -  - 那么问题就变成：是否存在一种方案，可以完成这 k 个任务？
     * -
     * - 一旦这样假设，问题结构发生了根本变化：
     * -
     * - 1）既然目标是完成 k 个任务：
     * -    - 那么必然选择“最轻的 k 个任务”
     * -      （如果能完成更重的，却完不成更轻的，可以交换，解只会更容易）
     * -
     * - 2）同时，只需要考虑“力量最大的 k 个工人”
     * -    - 更弱的工人不可能比更强的更有用
     * -
     * - 3）最重要的一点：
     * -    - 每一个被选中的任务，都【必须】被搬运
     * -    - “是否跳过某个任务”这个决策被彻底消除
     * -
     * - 这一步极其关键：
     * -  - 它直接消除了前面最复杂的“要不要搬”的分支
     * -  - 问题从“构造一个最优解”变成了“验证一个解是否存在”
     * -
     * - 这也是这一思路难以想到的原因：
     * -  - 人在做贪心题时，天然更倾向于一步步做决策
     * -  - 而这里恰恰需要先假设结果成立，再验证可行性
     * -
     * - 三、固定 k 之后，问题是否已经完全解决？
     * -
     * - 并没有，如下例子：
     * - 比如工人A力量= 6，工人B力量= 5
     * - 药水强度= 5
     * - 货物W1重量为10,货物W2重量为6
     * - 工人A和工人B都无法搬运货物W1和W2,喝药后都可以搬运，此时：
     * - - 给工人A用，可以搬运货物W1，但是工人B = 5 < 货物W2 = 6，最终只能搬运1个
     * - - 给工人B用，可以搬运货物W1，工人A = 6 >= 货物W2 = 6，最终可以搬运2个
     * -
     * - 即使固定了：
     * -  - k 个最轻的任务
     * -  - k 个最强的工人
     * - 依旧存在一个核心问题：
     * -  - 药水到底应该给谁用？
     * -
     * - 如果继续从“工人视角”分析，依旧会很复杂，
     * - 因此需要再次切换视角。
     * -
     * - 四、视角转换：从“工人”转为“任务（货物）”
     * -
     * - 固定 k 后，更自然的方式是：
     * -  - 按任务从重到轻依次尝试分配
     * -
     * - 对于当前最重的任务 w，只考虑一个问题：
     * -  - 在剩余工人中，是否有人能完成它？
     * -
     * - 此时有两种情况：
     * - 1）存在工人不吃药就能搬运 w：
     * -    - 由于后续任务都更轻
     * -    - 这些工人未来的可行范围完全相同
     * -    - 因此：谁来搬当前任务都不会影响最终可行性
     * - 2）没人能直接搬运 w：
     * -    - 那么必须使用药水
     * -    - 此时关键结论是：
     * -      必须让“喝完药后，刚好能搬运 w 的最弱工人”来搬
     * -
     * - 原因是：
     * -  - 如果让更强的工人喝药，会浪费其天然覆盖更广的能力
     * -  - 而较弱工人喝药后，如果已经能完成当前最重任务，
     * -    那么对后续更轻任务同样具备完整覆盖能力
     * -
     * - 因此可以统一为一个贪心原则：
     * -
     * -  - 对当前任务，从“可完成该任务的工人集合”中，
     * -    始终选择【力量最小】的那个
     * -  - 不区分是否吃药，本质上是同一条规则
     * -
     * - 五、算法层面的实现与优化
     * -
     * - 1）对于固定的 k：
     * -    - 维护一个有序的“剩余工人集合”
     * -    - 每次对当前最重任务：
     * -        - 优先找 ≥ w 的最小工人
     * -        - 若不存在，再找 ≥ (w - strength) 的最小工人并消耗药水
     * -
     * - 2）由于“是否能完成 k 个任务”具有单调性：
     * -    - 能完成 k，则一定能完成 k-1
     * -    - 不能完成 k，则一定不能完成 k+1
     * -
     * - 3）这里快速从集合中找到 ≥ 某个元素的最小元素，使用的是TreeMap，O(logN)
     * -    - 最开始的想法是使用一个单调栈，每次不断弹出，直到找到最小可完成元素，然后再一次入栈，复杂度较高
     * -    - 从这里看也可以看到使用TreeMap有些浪费，后续官方题解则使用Deque优化了
     * -
     * - 因此可以对 k 进行二分查找：
     * -  - 将外层枚举从 O(N) 优化为 O(log N)
     * -
     * - 六、最终整体思路总结
     * -
     * - 1）排序任务和工人
     * - 2）二分枚举答案 k
     * - 3）对每个 k：
     * -    - 取最轻的 k 个任务
     * -    - 取最强的 k 个工人
     * -    - 从任务角度（重 → 轻）进行贪心匹配
     * -    - 始终选择“刚好能完成当前任务的最弱工人”
     * -
     * - 整个解法的关键不在于数据结构本身，
     * - 而在于两次视角切换：
     * -  - 从“在线决策”转为“离线可行性验证”
     * -  - 从“以工人为中心”转为“以任务为中心”
     */
    public int maxTaskAssign(int[] tasks, int[] workers, int pills, int strength) {
        // 排序
        Arrays.sort(tasks);
        Arrays.sort(workers);
        // 最多可完成Min(工人数，任务数)个任务
        int left = 0, right = Math.min(tasks.length, workers.length);
        while (left < right) {
            // 通过二分优化枚举k的次数，使外层枚举k次数由原来的从大到小的O(n)变成O(logn)
            int mid = left + (right - left + 1) / 2;
            // 如果可以安排mid个任务，那么尝试安排更多任务，即从右区间找，否则尝试安排更少的任务，即从左区间找
            if (check(tasks, workers, pills, strength, mid)) {
                left = mid;
            } else {
                right = mid - 1;
            }
        }
        return left;
    }

    public boolean check(int[] tasks, int[] workers, int pills, int strength, int k) {
        TreeMap<Integer, Integer> map = new TreeMap<>();
        // 将力量较大的k个WORKER先放入map中
        for (int i = workers.length - 1; i >= workers.length - k; i--) {
            map.put(workers[i], map.getOrDefault(workers[i], 0) + 1);
        }
        // 尝试安排k个任务
        for (int i = k - 1; i >= 0; i--) {
            // 当前最重的货物重量
            int w = tasks[i];
            // todo：ceilingKey 和 floorKey 的区别 以及对应数据结构
            // 这里ceilingKey是获取键值大于等于tasks[i]的最小键值，如果存在，则返回该键值，否则返回null
            Integer work = map.ceilingKey(w);
            // 不嗑药没有工人可以搬起，则尝试嗑药
            if (work == null) {
                // 没有药了，无法搬起当前石头，结束
                if (pills <= 0) {
                    return false;
                }
                work = map.ceilingKey(w - strength);
                // 嗑药后依旧无法搬起当前石头，结束
                if (work == null) {
                    return false;
                }
                // 嗑药成功，药品-1，继续尝试
                pills--;
            }
            if (map.get(work) == 1) {
                map.remove(work);
            } else {
                map.put(work, map.get(work) - 1);
            }
        }
        return true;
    }


    /**
     * - leetCode官方题解：使用Deque优化TreeMap
     * -
     * - 一、从核心不变量开始：我们到底在维护什么？
     * -
     * - 在固定 k 个货物、k 个工人的前提下，问题已经被约束为：
     * - - 每一个货物都“必须被搬运”
     * - - 不再存在“是否跳过某个货物”的分支选择
     * -
     * - 在此前的推导中，已经得出了一个极其关键的不变量：
     * -
     * -   对于当前货物，只要存在一批“可以搬运它的工人”
     * -   （包括：不吃药即可搬运，或吃药后可以搬运），
     * -   那么最优策略一定是：
     * -   —— 让【力量最小的可行工人】去搬运这件货物。
     * -
     * - 这个结论意味着：
     * - - 我们并不关心所有工人
     * - - 也不关心“最接近某个阈值的工人”
     * - - 只关心：
     * -     1）当前有哪些工人是“可行的”
     * -     2）在这批人里，谁最弱、谁最强
     * -
     * - 换句话说：我们需要的是“顺序 + 两端选择”，而不是“精确查找”。
     * -
     * - 二、TreeMap / multiset 在这里多提供了哪些“用不上的能力”
     * -
     * - 如果使用 TreeMap / TreeSet / multiset，本质上是在使用：
     * - - 动态维护一个全局有序集合
     * - - 支持任意 key 的 ceiling / floor 查询
     * - - 支持任意位置的插入和删除
     * -
     * - 但结合上面的不变量重新审视需求，会发现：
     * - - 我们不需要对“任意值”做 ceilingKey
     * - - 不需要在集合中间位置反复调整
     * - - 不需要复杂的平衡维护
     * -
     * - 实际需求只有：
     * - - 工人按力量排序后，单调地“进入候选集合”
     * - - 每次决策，只会发生在：
     * -     - 最弱的可行工人
     * -     - 或最强的可行工人
     * -
     * - 因此，TreeMap 的能力是“过剩的”：
     * - 它解决的是一个比当前问题更通用的问题。
     * -
     * - 三、Deque 解法的真正抽象：双端候选池
     * -
     * - 官方解法的核心抽象并不是“换了一个数据结构”，
     * - 而是换了一个视角：
     * -   —— 维护一个【双端候选池】，里面只放“当前货物下可能参与搬运的工人”。
     * -
     * - 这个候选池具有以下特征：
     * - 1）工人是按力量排序后，单调加入的
     * - 2）一旦加入候选池，就不会因为后续货物而失效
     * -    （因为货物是从重到轻处理的）
     * - 3）候选池天然有序：
     * -    - 队头：力量最小的工人
     * -    - 队尾：力量最大的工人
     * -
     * - 由于“加入是单调的、使用是一次性的”，
     * - 候选池根本不需要平衡树，只需要一个 Deque。
     * -
     * - 四、Deque 如何同时处理「吃药 / 不吃药」两种情况
     * -
     * - 在此前的推导中，已经统一了两种情况的最优决策：
     * -
     * - 1）如果存在不吃药就能搬运当前货物的工人：
     * -    - 药水应该被保留
     * -    - 随意选一个不吃药即可完成任务的工人
     * -    - 为了不浪费“弱工人”，最合理的是使用【最强的工人】
     * -
     * - 2）如果必须吃药才能搬运：
     * -    - 吃药本质是“放大力量”
     * -    - 放大作用在弱工人身上收益最大
     * -    - 因此必须使用【力量最弱的可行工人】
     * -
     * - 在 Deque 中，这两种决策被自然映射为：
     * -
     * - - 不吃药能搬：从队尾 pop（最强）
     * - - 吃药才能搬：从队头 pop（最弱）
     * -
     * - 这正是 Deque 的“双端”存在意义，
     * - 而不是一个技巧性的选择。
     * -
     * - 五、为什么 Deque 比 TreeMap 再快一层
     * -
     * - 从复杂度角度看：
     * - - TreeMap / multiset：
     * -   每个工人插入、删除都需要 O(log n)
     * -   整体复杂度为 O(n log n)
     * - - Deque：
     * -   每个工人：
     * -     - 只会进入候选池一次
     * -     - 只会被使用（弹出）一次
     * -   每次操作都是 O(1)
     * -
     * - 因此，在固定 k 校验这一层：
     * - - 数据结构操作整体是 O(n)
     * - - 外层只剩下对 k 的二分搜索：O(log n)
     * -
     * - 本质上，这是一次“能力降级优化”：
     * -   从“通用有序结构”
     * -   → “单调推进 + 双端选择”
     */
    class Solution {
        public int maxTaskAssign(int[] tasks, int[] workers, int pills, int strength) {
            int n = tasks.length, m = workers.length;
            Arrays.sort(tasks);
            Arrays.sort(workers);
            int left = 1, right = Math.min(m, n), ans = 0;
            while (left <= right) {
                int mid = (left + right) / 2;
                if (check(tasks, workers, pills, strength, mid)) {
                    ans = mid;
                    left = mid + 1;
                } else {
                    right = mid - 1;
                }
            }
            return ans;
        }

        // 检查是否可以在mid个任务中使用pills和strength
        private boolean check(int[] tasks, int[] workers, int pills, int strength, int mid) {
            int p = pills;
            int m = workers.length;
            // ws 中的工人，满足：喝药 or 不喝药，都“有可能”完成当前任务
            Deque<Integer> ws = new ArrayDeque<>();
            int ptr = m - 1;
            // 从大到小枚举每一个任务
            for (int i = mid - 1; i >= 0; --i) {
                // 将嗑药后可以完成当前工作的工人加入双端队列（相当于嗑不嗑药都能完成的都加入了）
                while (ptr >= m - mid && workers[ptr] + strength >= tasks[i]) {
                    ws.addFirst(workers[ptr]);
                    --ptr;
                }
                // 没人可以完成当前任务，结束
                if (ws.isEmpty()) {
                    return false;
                }
                // 有工人不嗑药也能完成，直接使用该工人，对应之前分析的，只要有人能完成，这些人里随便选一个完成都可以，且不影响后续结果
                else if (ws.getLast() >= tasks[i]) {
                    // 如果双端队列中最大的元素大于等于 tasks[i]
                    ws.pollLast();
                } else {
                    // 药已用完，无法完成当前任务
                    if (p == 0) {
                        return false;
                    }
                    // 药品-1，使用吃药后能完成任务里的最弱工人
                    --p;
                    ws.pollFirst();
                }
            }
            return true;
        }
    }
}
